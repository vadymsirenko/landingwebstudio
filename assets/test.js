const fallbackLang = "en";
const storageKey = "lws-lang";

function resolveTranslation(langData, key) {
  return key.split(".").reduce((acc, part) => (acc && acc[part] !== undefined ? acc[part] : undefined), langData);
}

function applyTranslations(lang) {
  const langData = translations[lang] || translations[fallbackLang];
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const translation = resolveTranslation(langData, node.dataset.i18n);
    if (translation === undefined) {
      return;
    }
    if (node instanceof HTMLInputElement) {
      node.placeholder = translation;
    } else if (node instanceof HTMLTextAreaElement) {
      node.placeholder = translation;
    } else {
      node.textContent = translation;
    }
  });
}

function setLanguage(lang) {
  const language = translations[lang] ? lang : fallbackLang;
  document.documentElement.lang = language;
  localStorage.setItem(storageKey, language);
  const toggle = document.getElementById("language-toggle");
  if (toggle) {
    toggle.value = language;
  }
  applyTranslations(language);
}

document.addEventListener("DOMContentLoaded", () => {
  const storedLang = localStorage.getItem(storageKey) || navigator.language?.slice(0, 2) || fallbackLang;
  setLanguage(storedLang);

  const toggle = document.getElementById("language-toggle");
  if (toggle) {
    toggle.addEventListener("change", (event) => {
      setLanguage(event.target.value);
    });
  }
});
